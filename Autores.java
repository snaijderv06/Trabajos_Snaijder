    private void jBEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditarActionPerformed
        int fila=this.jTableAutores.getSelectedRow();
        if (fila==-1){
            JOptionPane.showMessageDialog(rootPane, "Seleccione un registro de la tabla");
        }
        else{
            try{
            int id=Integer.parseInt((String)this.jTableAutores.getValueAt(fila,0).toString());
            String nom=(String)this.jTableAutores.getValueAt(fila, 1);
            String ape=(String)this.jTableAutores.getValueAt(fila, 2);
            String corr=(String)this.jTableAutores.getValueAt(fila, 3);
            String ced=(String)this.jTableAutores.getValueAt(fila, 4);
            Date fec=Date.valueOf((String)this.jTableAutores.getValueAt(fila, 5).toString());
            
            jTextIdAutores.setText(""+id);
            jTextNombres.setText(nom);
            jTextApellidos.setText(ape);
            jTextEmail.setText(corr);
            jTextCedula.setText(ced);
            jTextFechaNac.setText(String.valueOf(fec));
            }catch(NumberFormatException e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jBEditarActionPerformed
